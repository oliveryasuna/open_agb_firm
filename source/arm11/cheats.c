#define CHEAT_FILE_BUF_SIZE (2048u)

// --- PARv3 format constants (from mGBA) ---

// Condition field (bits 27-29).
#define PAR3_COND 0x38000000

// Width field (bits 25-26).
#define PAR3_WIDTH 0x06000000
#define PAR3_WIDTH_BASE 25

// Base operation (bits 30-31, overlaps with action for conditionals).
#define PAR3_BASE 0xC0000000
#define PAR3_BASE_ASSIGN 0x00000000

// Special sub-types (when op1 == 0, keyed on op2's top byte).
#define PAR3_OTHER_PATCH_1 0x18000000
#define PAR3_OTHER_PATCH_2 0x1A000000
#define PAR3_OTHER_PATCH_3 0x1C000000
#define PAR3_OTHER_PATCH_4 0x1E000000

// GBA ROM base address for patch calculations.
#define GBA_BASE_ROM0 0x08000000

// --- PARv3 address remapping ---

static u32 parAddr(u32 x) {
  return (x & 0xFFFFF) | ((x << 4) & 0x0F000000);
}

// --- TEA decryption ---

