// Cheat file parser. Reads Action Replay / Pro Action Replay v3
// codes from a text file, decrypts them via TEA, and processes them.
//
// RAM writes are stored for the ARM7 VBlank cheat engine.
// ROM patches are applied directly to the ROM in FCRAM.

