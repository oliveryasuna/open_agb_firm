static void teaDecrypt(u32 *op1, u32 *op2, const u32 seeds[4]) {
  u32 sum = 0xC6EF3720;
  for (u32 i = 0; i < 32; i++) {
    *op2 -= ((*op1 << 4) + seeds[2]) ^ (*op1 + sum) ^ ((*op1 >> 5) + seeds[3]);
    *op1 -= ((*op2 << 4) + seeds[0]) ^ (*op2 + sum) ^ ((*op2 >> 5) + seeds[1]);
    sum -= 0x9E3779B9;
  }
}

static void reseedParv3(u32 *seeds, u16 params) {
  const u32 s0 = params >> 8;
  const u32 s1 = params & 0xFF;
  for (u32 y = 0; y < 4; y++) {
    for (u32 x = 0; x < 4; x++) {
      u8 z = par3T1[(s0 + x) & 0xFF] + par3T2[(s1 + y) & 0xFF];
      seeds[y] <<= 8;
      seeds[y] |= z;
    }
  }
}

// --- Cheat storage ---

// RAM cheats: applied by ARM7 engine every VBlank.
static u32 g_ramCheats[LGY_MAX_CHEATS * 2];
static u32 g_ramCheatCount = 0;

// Add a RAM cheat entry in the ARM7 engine format.
static void addRamCheat(u32 address, u32 value, u32 width) {
  if (g_ramCheatCount >= LGY_MAX_CHEATS)
    return;

  // Encode type nibble: 0 = 8-bit, 1 = 16-bit, 2 = 32-bit.
  u32 type;
  switch (width) {
  case 1:
    type = 0;
    break;
  case 2:
    type = 1;
    break;
  default:
    type = 2;
    break;
  }

  g_ramCheats[g_ramCheatCount * 2] = (type << 28) | (address & 0x0FFFFFFF);
  g_ramCheats[g_ramCheatCount * 2 + 1] = value;
  g_ramCheatCount++;
}

// Apply a ROM patch directly to FCRAM.
static void applyRomPatch(u32 address, u16 value) {
  // address is a GBA ROM address (0x08XXXXXX). Convert to FCRAM offset.
  const u32 offset = address & (LGY_MAX_ROM_SIZE - 1);
  volatile u16 *rom = (volatile u16 *)(LGY_ROM_LOC + offset);
  *rom = value;
}

// --- PARv3 code processing ---

// Parser state for multi-code sequences.
static bool g_pendingRomPatch;
static u32 g_romPatchAddr;
static u32 g_romPatchWidth;

static void processPar3Special(u32 op2) {
  const u32 subType = op2 & 0xFE000000;

  if (subType >= PAR3_OTHER_PATCH_1 && subType <= PAR3_OTHER_PATCH_4) {
    // ROM patch: next code's op1 is the value.
    g_pendingRomPatch = true;
    g_romPatchAddr = GBA_BASE_ROM0 | ((op2 & 0xFFFFFF) << 1);
    g_romPatchWidth = 2; // All ROM patches are 16-bit.
    return;
  }

  // Other special codes (fill, button, etc.) are not yet supported.
}

static void processPar3Code(u32 op1, u32 op2) {
  // Handle pending ROM patch (previous code set up the address).
  if (g_pendingRomPatch) {
    g_pendingRomPatch = false;
    applyRomPatch(g_romPatchAddr, (u16)op1);
    return;
  }

  // DEADFACE: reseed encryption keys.
  if (op1 == 0xDEADFACE) {
    reseedParv3(parv3Seeds, (u16)op2);
    return;
  }

  // Master code identifier — skip.
  if (op2 == 0x001DC0DE) {
    return;
  }

  // Special codes (op1 == 0): ROM patches, fills, buttons, etc.
  if (op1 == 0x00000000) {
    processPar3Special(op2);
    return;
  }

  // Conditional codes — not yet supported, skip.
  if (op1 & PAR3_COND) {
    return;
  }

  // Hook codes (0xC4) — not applicable for bare metal, skip.
  if ((op1 >> 24) == 0xC4) {
    return;
  }

  // Simple write: base operation is ASSIGN with no condition.
  if ((op1 & PAR3_BASE) == PAR3_BASE_ASSIGN) {
    const u32 width = 1u << ((op1 & PAR3_WIDTH) >> PAR3_WIDTH_BASE);
    const u32 address = parAddr(op1);
    const u32 mask = (width < 4) ? ((1u << (width * 8)) - 1) : 0xFFFFFFFF;
    addRamCheat(address, op2 & mask, width);
    return;
  }

  // Other base types (indirect, add, IO) are not yet supported.
}

// --- Public API ---

Result loadCheats(const char *const path) {
  g_ramCheatCount = 0;
  g_pendingRomPatch = false;

  // Reset PARv3 seeds to defaults.
  parv3Seeds[0] = 0x7AA9648F;
  parv3Seeds[1] = 0x7FAE6994;
  parv3Seeds[2] = 0xC0EFAAD5;
  parv3Seeds[3] = 0x42712C57;

  char *buf = (char *)calloc(CHEAT_FILE_BUF_SIZE, 1);
  if (buf == NULL) {
    return RES_OUT_OF_MEM;
  }

  Result res = fsQuickRead(path, buf, CHEAT_FILE_BUF_SIZE - 1);
  if (res == RES_FR_NO_FILE) {
    free(buf);
    return RES_OK;
  }
  if (res != RES_OK) {
    free(buf);
    return res;
  }

  // Parse lines of the form "XXXXXXXX YYYYYYYY".
  // Lines starting with '#' or ';' are comments.
  char *line = buf;
  while (*line) {
    // Skip whitespace and newlines.
    while (*line == ' ' || *line == '\t' || *line == '\r' || *line == '\n')
      line++;

    if (*line == '\0')
      break;

    // Skip comment lines.
    if (*line == '#' || *line == ';') {
      while (*line && *line != '\n')
        line++;
      continue;
    }

    // Parse two hex values.
    char *end;
    u32 op1 = strtoul(line, &end, 16);
    if (end == line)
      break;
    line = end;

    while (*line == ' ' || *line == '\t')
      line++;

    u32 op2 = strtoul(line, &end, 16);
    if (end == line)
      break;
    line = end;

    // Decrypt as PARv3.
    teaDecrypt(&op1, &op2, parv3Seeds);

    // Process the decrypted PARv3 code.
    processPar3Code(op1, op2);
  }

  free(buf);
  return RES_OK;
}

u32 getCheatCount(void) { return g_ramCheatCount; }

const u32 *getCheatData(void) { return g_ramCheats; }
